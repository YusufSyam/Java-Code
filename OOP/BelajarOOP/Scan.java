package BelajarOOP;

import java.util.Scanner;

public class Scan {
	static Scanner xx= new Scanner(System.in);
	
	static String String() {
		return xx.nextLine();
	}
	
	static long Long() {
		return xx.nextLong();
	}
	
	static int Int() {
		return xx.nextInt();
	}
	
	static double Double() {
		return xx.nextDouble();
	}
	
	static float Float() {
		return xx.nextFloat();
	}
	
	
}
