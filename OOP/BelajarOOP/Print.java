package BelajarOOP;

public class Print {

	public static void Jing(String a){
		System.out.print(a);	
	}
	
	public static void Jing(String a, String b){
		System.out.print(a);	
		System.out.print(b);	
	}
	
	public static void Jing(String a, String b, String c){
		System.out.println(a);
		System.out.println(b);	
		System.out.println(c);	
	}
	
	public static void Jingln(String a){
		System.out.println(a);	
	}
	
	public static void Jingln(String a, String b){
		System.out.println(a);	
		System.out.println(b);	
	}
	
	public static void Jingln(String a, String b, String c){
		System.out.println(a);
		System.out.println(b);	
		System.out.println(c);	
	}
	
	public static void Jingln(String a, int b, double c){
		System.out.println(a);
		System.out.println(b);	
		System.out.println(c);	
	}
}
